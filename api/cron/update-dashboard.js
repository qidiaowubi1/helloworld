import { put } from "@vercel/blob";
import { buildLiveDashboard } from "../dashboard.js";

export default async function handler(request, response) {
  const cronSecret = process.env.CRON_SECRET;
  const authHeader = request.headers.authorization;

  if (cronSecret && authHeader !== `Bearer ${cronSecret}`) {
    return response.status(401).json({ ok: false, error: "Unauthorized" });
  }

  try {
    const snapshot = await buildLiveDashboard();
    snapshot.meta = {
      ...snapshot.meta,
      version: "0.2.3",
      message: "Cloud snapshot generated by Vercel Cron and stored in Vercel Blob.",
      cloudSource: "vercel-cron",
      cronSchedule: request.headers["x-vercel-cron-schedule"] || null
    };

    const pathname = process.env.DASHBOARD_BLOB_PATH || "dashboard/latest.json";
    const blob = await put(pathname, JSON.stringify(snapshot, null, 2), {
      access: "public",
      allowOverwrite: true,
      contentType: "application/json",
      cacheControlMaxAge: 60
    });

    return response.status(200).json({
      ok: true,
      updatedAt: snapshot.meta.lastUpdated,
      blobUrl: blob.url,
      pathname
    });
  } catch (error) {
    return response.status(500).json({
      ok: false,
      error: error instanceof Error ? error.message : "Cron update failed"
    });
  }
}
